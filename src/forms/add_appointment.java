    public void time() {
        
        t = new Timer(0,new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date dt = new Date();
                st = new SimpleDateFormat("hh:mm a");
                
                String tt = st.format(dt);
                txtTime.setText(tt);
            }
        });
        t.start();
    }
    
